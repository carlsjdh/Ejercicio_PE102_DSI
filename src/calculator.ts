export class Calculator {
  constructor() {
  }

  /**
   * add
   * @param number1
   * @param number2
   * @returns
   */
  add(number1 :number, number2 :number) :number {
    return number1 + number2;
  };
  /**
   * substrtaction
   * @param number1
   * @param number2
   * @returns
   */
  substraction(number1 :number, number2 :number) :number {
    return number1 - number2;
  };
};

